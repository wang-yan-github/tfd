package tfd.system.unit.org.data;

public class Unit {
	private int id;
	private String orgId;
	private String orgName;
	private String orgAdd;
	private String orgTel;
	private String orgFax;
	private String orgPost;
	private String orgEmail;
	public String getOrgEmail() {
		return orgEmail;
	}
	public void setOrgEmail(String orgEmail) {
		this.orgEmail = orgEmail;
	}
	public String getOrgPost() {
		return orgPost;
	}
	public void setOrgPost(String orgPost) {
		this.orgPost = orgPost;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public String getOrgAdd() {
		return orgAdd;
	}
	public void setOrgAdd(String orgAdd) {
		this.orgAdd = orgAdd;
	}
	public String getOrgTel() {
		return orgTel;
	}
	public void setOrgTel(String orgTel) {
		this.orgTel = orgTel;
	}
	public String getOrgFax() {
		return orgFax;
	}
	public void setOrgFax(String orgFax) {
		this.orgFax = orgFax;
	}

}
