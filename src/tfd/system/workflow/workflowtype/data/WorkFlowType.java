package tfd.system.workflow.workflowtype.data;

public class WorkFlowType {
private int Id;
private String flowTypeId;
private String flowTypeName;
private String moduleId;
private String manageAccountId;
private String leaveId;
private String topFlag;
private String orgId;
private String sortId;
public int getId() {
	return Id;
}
public void setId(int id) {
	Id = id;
}
public String getFlowTypeId() {
	return flowTypeId;
}
public void setFlowTypeId(String flowTypeId) {
	this.flowTypeId = flowTypeId;
}
public String getFlowTypeName() {
	return flowTypeName;
}
public void setFlowTypeName(String flowTypeName) {
	this.flowTypeName = flowTypeName;
}
public String getModuleId() {
	return moduleId;
}
public void setModuleId(String moduleId) {
	this.moduleId = moduleId;
}
public String getManageAccountId() {
	return manageAccountId;
}
public void setManageAccountId(String manageAccountId) {
	this.manageAccountId = manageAccountId;
}
public String getLeaveId() {
	return leaveId;
}
public void setLeaveId(String leaveId) {
	this.leaveId = leaveId;
}
public String getTopFlag() {
	return topFlag;
}
public void setTopFlag(String topFlag) {
	this.topFlag = topFlag;
}
public String getOrgId() {
	return orgId;
}
public void setOrgId(String orgId) {
	this.orgId = orgId;
}
public String getSortId() {
	return sortId;
}
public void setSortId(String sortId) {
	this.sortId = sortId;
}
}
