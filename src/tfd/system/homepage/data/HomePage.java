package tfd.system.homepage.data;

public class HomePage {
	private int id;
	private String homePageId;
	private String module;
	private String moduleName;
	private String addressTd;
	private String addressTr;
	private String isOpen;
	private String accountId;
	private String orgId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getHomePageId() {
		return homePageId;
	}
	public void setHomePageId(String homePageId) {
		this.homePageId = homePageId;
	}
	public String getModule() {
		return module;
	}
	public void setModule(String module) {
		this.module = module;
	}
	public String getModuleName() {
		return moduleName;
	}
	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}
	public String getAddressTd() {
		return addressTd;
	}
	public void setAddressTd(String addressTd) {
		this.addressTd = addressTd;
	}
	public String getAddressTr() {
		return addressTr;
	}
	public void setAddressTr(String addressTr) {
		this.addressTr = addressTr;
	}
	public String getIsOpen() {
		return isOpen;
	}
	public void setIsOpen(String isOpen) {
		this.isOpen = isOpen;
	}
	public String getAccountId() {
		return accountId;
	}
	public void setAccountId(String accountId) {
		this.accountId = accountId;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
	public HomePage() {
		super();
	}
	
}
