package tfd.system.charts.data;

public class Charts {
	private int id;
	private String chartsId;
	private String chartsName;
	private String chartsModule;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getChartsId() {
		return chartsId;
	}
	public void setChartsId(String chartsId) {
		this.chartsId = chartsId;
	}
	public String getChartsName() {
		return chartsName;
	}
	public void setChartsName(String chartsName) {
		this.chartsName = chartsName;
	}
	public String getChartsModule() {
		return chartsModule;
	}
	public void setChartsModule(String chartsModule) {
		this.chartsModule = chartsModule;
	}
	public Charts() {
		super();
	}
	
}
