package tfd.system.charts.data;

public class ChartsConfig {
	private int id;
	private String chartsConfigId;
	private String chartsId;
	private String accountId;
	private String deptId;
	private String privId;
	private String orgId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getChartsConfigId() {
		return chartsConfigId;
	}
	public void setChartsConfigId(String chartsConfigId) {
		this.chartsConfigId = chartsConfigId;
	}
	public String getChartsId() {
		return chartsId;
	}
	public void setChartsId(String chartsId) {
		this.chartsId = chartsId;
	}
	public String getAccountId() {
		return accountId;
	}
	public void setAccountId(String accountId) {
		this.accountId = accountId;
	}
	public String getDeptId() {
		return deptId;
	}
	public void setDeptId(String deptId) {
		this.deptId = deptId;
	}
	public String getPrivId() {
		return privId;
	}
	public void setPrivId(String privId) {
		this.privId = privId;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
	public ChartsConfig() {
		super();
	}
}
