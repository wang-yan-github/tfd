package tfd.system.attend.data;

public class AttendRegist {
	private int id;
	private String attendRegistId;
	private String beforeWork;
	private String afterWork;
	private String beforeBack;
	private String afterBack;
	private String orgId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAttendRegistId() {
		return attendRegistId;
	}
	public void setAttendRegistId(String attendRegistId) {
		this.attendRegistId = attendRegistId;
	}
	public String getBeforeWork() {
		return beforeWork;
	}
	public void setBeforeWork(String beforeWork) {
		this.beforeWork = beforeWork;
	}
	public String getAfterWork() {
		return afterWork;
	}
	public void setAfterWork(String afterWork) {
		this.afterWork = afterWork;
	}
	public String getBeforeBack() {
		return beforeBack;
	}
	public void setBeforeBack(String beforeBack) {
		this.beforeBack = beforeBack;
	}
	public String getAfterBack() {
		return afterBack;
	}
	public void setAfterBack(String afterBack) {
		this.afterBack = afterBack;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
	public AttendRegist() {
		super();
	}
	
	
}
