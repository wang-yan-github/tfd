package officesupplies.data;

public class Offlibrary {

	private int Id;
	private String libraryId;
	private String libraryName;
	private String belongDept;
	private String libraryStaff;
	private String dispatchStaff;
	private String delStatus;
	private String topId;
	private String sortId;
	private String orgId;
	public String getTopId() {
		return topId;
	}
	public void setTopId(String topId) {
		this.topId = topId;
	}
	public String getSortId() {
		return sortId;
	}
	public void setSortId(String sortId) {
		this.sortId = sortId;
	}
	
	public String getDelStatus() {
		return delStatus;
	}
	public void setDelStatus(String delStatus) {
		this.delStatus = delStatus;
	}
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getLibraryId() {
		return libraryId;
	}
	public void setLibraryId(String libraryId) {
		this.libraryId = libraryId;
	}
	public String getLibraryName() {
		return libraryName;
	}
	public void setLibraryName(String libraryName) {
		this.libraryName = libraryName;
	}
	public String getBelongDept() {
		return belongDept;
	}
	public void setBelongDept(String belongDept) {
		this.belongDept = belongDept;
	}
	public String getLibraryStaff() {
		return libraryStaff;
	}
	public void setLibraryStaff(String libraryStaff) {
		this.libraryStaff = libraryStaff;
	}
	public String getDispatchStaff() {
		return dispatchStaff;
	}
	public void setDispatchStaff(String dispatchStaff) {
		this.dispatchStaff = dispatchStaff;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
	
}
