package meeting.data;

public class Boardroom {
	private int id;
	private String boardroomId;
	private String boardroomName;
	private String boardroomDepict;
	private String boardroomStaff;
	private String deptPriv;
	private String userPriv;
	private String boardroomNum;
	private String allowTime;
	private String equipment;
	private String boardroomAddress;
	private String boardroomSystem;
	private String orgId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getBoardroomId() {
		return boardroomId;
	}
	public void setBoardroomId(String boardroomId) {
		this.boardroomId = boardroomId;
	}
	public String getBoardroomName() {
		return boardroomName;
	}
	public void setBoardroomName(String boardroomName) {
		this.boardroomName = boardroomName;
	}
	public String getBoardroomDepict() {
		return boardroomDepict;
	}
	public void setBoardroomDepict(String boardroomDepict) {
		this.boardroomDepict = boardroomDepict;
	}
	public String getBoardroomStaff() {
		return boardroomStaff;
	}
	public void setBoardroomStaff(String boardroomStaff) {
		this.boardroomStaff = boardroomStaff;
	}
	public String getDeptPriv() {
		return deptPriv;
	}
	public void setDeptPriv(String deptPriv) {
		this.deptPriv = deptPriv;
	}
	public String getUserPriv() {
		return userPriv;
	}
	public void setUserPriv(String userPriv) {
		this.userPriv = userPriv;
	}
	public String getBoardroomNum() {
		return boardroomNum;
	}
	public void setBoardroomNum(String boardroomNum) {
		this.boardroomNum = boardroomNum;
	}
	public String getAllowTime() {
		return allowTime;
	}
	public void setAllowTime(String allowTime) {
		this.allowTime = allowTime;
	}
	public String getEquipment() {
		return equipment;
	}
	public void setEquipment(String equipment) {
		this.equipment = equipment;
	}
	public String getBoardroomAddress() {
		return boardroomAddress;
	}
	public void setBoardroomAddress(String boardroomAddress) {
		this.boardroomAddress = boardroomAddress;
	}
	public String getBoardroomSystem() {
		return boardroomSystem;
	}
	public void setBoardroomSystem(String boardroomSystem) {
		this.boardroomSystem = boardroomSystem;
	}
	public String getOrgId() {
		return orgId;
	}
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}
	
	
}
